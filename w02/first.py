import turtle  # turtle 그래픽 모듈 불러오기

# 정사각형 그리기 (각 변의 길이: 100)
turtle.forward(100)   # 앞으로 100 이동
turtle.right(90)      # 오른쪽으로 90도 회전
turtle.forward(100)   # 앞으로 100 이동
turtle.right(90)      # 오른쪽으로 90도 회전
turtle.forward(100)   # 앞으로 100 이동
turtle.right(90)      # 오른쪽으로 90도 회전
turtle.forward(100)   # 앞으로 100 이동 (마지막 변)

# 별 그리기 (각 변의 길이: 100, 회전 각도: 144도)
turtle.forward(100)   # 앞으로 100 이동
turtle.right(144)     # 오른쪽으로 144도 회전
turtle.forward(100)   # 앞으로 100 이동
turtle.right(144)     # 오른쪽으로 144도 회전
turtle.forward(100)   # 앞으로 100 이동
turtle.right(144)     # 오른쪽으로 144도 회전
turtle.forward(100)   # 앞으로 100 이동
turtle.right(144)     # 오른쪽으로 144도 회전
turtle.forward(100)   # 앞으로 100 이동 (마지막 변)

turtle.done()  # turtle 그래픽 종료. 창이 닫히지 않도록 함.