"""
재귀 호출로 나무를 그리되, 가지의 두께와 각종 설정값을 상수로 분리한 예제.

`tree0.py`가 재귀 구조 자체를 보여주는 기본 예제였다면,
이 파일은 나무 모양을 더 자연스럽게 만들기 위해 다음 요소를 추가한다.
1. 가지가 갈라지는 각도
2. 재귀를 멈추는 최소 길이
3. 하위 가지의 길이 감소 비율
4. 가지 길이에 비례한 펜 두께

즉, 같은 재귀 구조라도 설정값을 조절하면 전혀 다른 모양의 나무를 만들 수 있음을 보여준다.
"""

import turtle as t

# 가지가 좌우로 갈라질 때 사용하는 회전 각도
ANGLE = 20

# 가지 길이가 이 값보다 작아지면 더 이상 재귀적으로 갈라지지 않는다.
MIN_LENGTH = 5

# 하위 가지 길이는 부모 가지 길이의 70%로 줄어든다.
REDUCE_RATE = 0.7

# 가지 길이에 비례해 펜 두께를 정하기 위한 비율
WIDTH_RATE = 0.1

def tree(length):
	# 긴 줄기는 굵게, 짧은 줄기는 가늘게 그려서 더 자연스러운 나무처럼 보이게 한다.
	t.width(length * WIDTH_RATE)

	# 현재 길이만큼 앞으로 나아가며 줄기 또는 가지 하나를 그린다.
	t.forward(length)

	# 길이가 충분히 남아 있을 때만 좌우 가지를 계속 만들어 낸다.
	if length > MIN_LENGTH:
		# 다음 단계 가지는 더 짧아진다.
		sub = length * REDUCE_RATE

		# 왼쪽 가지를 그린다.
		t.left(ANGLE)
		tree(sub)

		# 오른쪽 가지를 그리기 위해 반대쪽으로 회전한다.
		t.right(2 * ANGLE)
		tree(sub)

		# 상위 호출의 방향을 유지하기 위해 처음 방향으로 되돌린다.
		t.left(ANGLE)

	# 현재 호출이 시작된 위치로 되돌아가야 다른 가지를 이어서 그릴 수 있다.
	t.backward(length)


# 그리는 과정을 빠르게 보기 위해 최고 속도로 설정한다.
t.speed(0)

# 창 제목을 설정한다.
t.title("재귀 호출로 나무 그리기 #1: 두께 등 설정 값 추가")

# 화면 아래쪽에서 시작해 위로 자라는 나무처럼 보이게 시작 위치를 옮긴다.
t.penup()
t.goto(0, -200)
t.pendown()
t.setheading(90)

# 길이 120의 줄기에서 시작하여 재귀적으로 나무 전체를 그린다.
tree(120)

t.exitonclick()  # 창을 클릭하면 종료
