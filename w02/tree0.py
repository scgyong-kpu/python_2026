"""
재귀 호출을 사용해 나무 모양을 그리는 예제.

이 프로그램의 의도는 "같은 동작을 조금씩 줄여 가며 반복하면
복잡한 모양도 간단한 규칙으로 만들 수 있다"는 점을 보여주는 것이다.

한 번의 `tree(length)` 호출은 다음 일을 한다.
1. 현재 방향으로 줄기 또는 가지를 한 번 그린다.
2. 길이가 충분히 길면 좌우로 갈라지는 두 개의 하위 가지를 재귀적으로 그린다.
3. 하위 가지를 모두 그린 뒤에는 시작 위치로 되돌아온다.

이렇게 하면 각 호출이 "작은 나무 하나"를 맡게 되고,
그 작은 나무들이 다시 더 작은 나무 둘로 갈라지면서 전체 나무 모양이 만들어진다.
"""

import turtle as t

def tree(length):
    # 현재 길이만큼 앞으로 이동하며 줄기 또는 가지 하나를 그린다.
    t.forward(length)

    # 가지 길이가 너무 짧아지면 더 이상 갈라지지 않게 멈춘다.
    # 이것이 재귀 호출의 종료 조건이다.
    if length > 50:
        # 아래 단계의 가지는 조금 더 짧게 만들어 자연스럽게 작아지게 한다.
        sub = length * 0.8

        # 왼쪽으로 방향을 틀고 더 작은 가지를 그린다.
        t.left(20)
        tree(sub)

        # 오른쪽 가지를 그리기 위해 현재 방향에서 반대쪽으로 크게 회전한다.
        t.right(40)
        tree(sub)

        # 두 가지를 모두 그린 뒤 처음 방향으로 되돌린다.
        t.left(20)

    # 이 호출이 시작된 위치로 되돌아가야 상위 줄기에서 다른 가지를 이어 그릴 수 있다.
    # 즉, 앞으로 갔다가 다시 뒤로 오는 방식으로 "원위치 복귀"를 한다.
    t.backward(length)

# 필요하면 그리기 속도를 가장 빠르게 설정할 수 있다.
# t.speed(0)

# 나무가 위로 자라도록 거북이의 시작 방향을 위쪽으로 돌린다.
t.left(90)

# 길이 100인 큰 줄기에서 시작해 재귀적으로 나무 전체를 그린다.
tree(100)

t.exitonclick()  # 창을 클릭하면 종료
